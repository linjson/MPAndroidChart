    @Override
    public boolean onCreateOptionsMenu(Menu menu) {
        getMenuInflater().inflate(R.menu.pie, menu);
        return true;
    }

    @Override
    public boolean onOptionsItemSelected(MenuItem item) {

        switch (item.getItemId()) {
            case R.id.actionToggleValues: {
                for (IDataSet<?> set : mChart.getData().getDataSets())
                    set.setDrawValues(!set.isDrawValuesEnabled());

                mChart.invalidate();
                break;
            }
            case R.id.actionToggleHole: {
//                if (mChart.isDrawHoleEnabled())
//                    mChart.setDrawHoleEnabled(false);
//                else
//                    mChart.setDrawHoleEnabled(true);
//                mChart.invalidate();
//                break;
            }
            case R.id.actionDrawCenter: {
                if (mChart.isDrawCenterTextEnabled())
                    mChart.setDrawCenterText(false);
                else
                    mChart.setDrawCenterText(true);
                mChart.invalidate();
                break;
            }
            case R.id.actionToggleXVals: {

//                mChart.setDrawEntryLabels(!mChart.isDrawEntryLabelsEnabled());
                mChart.invalidate();
                break;
            }
            case R.id.actionSave: {
                // mChart.saveToGallery("title"+System.currentTimeMillis());
                mChart.saveToPath("title" + System.currentTimeMillis(), "");
                break;
            }
            case R.id.actionTogglePercent:
                mChart.setUsePercentValues(!mChart.isUsePercentValuesEnabled());
                mChart.invalidate();
                break;
            case R.id.animateX: {
                mChart.animateX(1400);
                break;
            }
            case R.id.animateY: {
                mChart.animateY(1400);
                break;
            }
            case R.id.animateXY: {
                mChart.animateXY(1400, 1400);
                break;
            }
            case R.id.actionToggleSpin: {
                mChart.spin(1000, mChart.getRotationAngle(), mChart.getRotationAngle() + 360, Easing.EasingOption
                        .EaseInCubic);
                break;
            }
        }
        return true;
    }

    @Override
    public void onProgressChanged(SeekBar seekBar, int progress, boolean fromUser) {

        tvX.setText("" + (mSeekBarX.getProgress()));
        tvY.setText("" + (mSeekBarY.getProgress()));
        mChart.setRotationAngle(mSeekBarX.getProgress() % 360);
        mChart.invalidate();
//        setData(mSeekBarX.getProgress(), mSeekBarY.getProgress());
    }

    private void setData(int count, float range) {

        float mult = range;
        ArrayList<IXPieDataSet> list = new ArrayList<>();

        ArrayList<PieEntry> x = new ArrayList<>();
        x.add(new PieEntry(20, "A"));
        x.add(new PieEntry(30, "B"));
        x.add(new PieEntry(50, "C"));


        XPieDataSet dataSet = new XPieDataSet(x, null);

        x = new ArrayList<>();

        x.add(new PieEntry(50, "D"));
        x.add(new PieEntry(50, "E"));
        XPieDataSet dataSet2 = new XPieDataSet(x, null);
        //dataSet.setSelectionShift(0f);

        dataSet.setColors(ColorTemplate.VORDIPLOM_COLORS);
        dataSet2.setColors(ColorTemplate.JOYFUL_COLORS);
        config(dataSet);
        config(dataSet2);


        dataSet2.setDrawHoleEnabled(false);
        list.add(dataSet);
        list.add(dataSet2);


        XPieData data = new XPieData(list);
        data.setValueFormatter(new PercentFormatter());
        data.setValueTextSize(11f);

        data.setValueTextColor(Color.BLACK);
        data.setValueTypeface(mTfLight);
        mChart.setData(data);

        // undo all highlights
        mChart.highlightValues(null);

        mChart.invalidate();
    }


    private void config(XPieDataSet dataSet) {
        dataSet.setXValuePosition(PieDataSet.ValuePosition.OUTSIDE_SLICE);
        dataSet.setYValuePosition(PieDataSet.ValuePosition.OUTSIDE_SLICE);
        dataSet.setValueLineColor(Color.RED);
        dataSet.setValueLinePart1Length(0.6f);
        dataSet.setValueLinePart2Length(0.4f);
        dataSet.setValueLinePart1OffsetPercentage(60f);

        dataSet.setSliceSpace(3f);
        dataSet.setSelectionShift(15f);

        dataSet.setDrawHoleEnabled(true);
        dataSet.setHoleColor(Color.WHITE);

        dataSet.setTransparentCircleColor(Color.WHITE);
        dataSet.setTransparentCircleAlpha(110);

        dataSet.setHoleRadius(70f);
        dataSet.setTransparentCircleRadius(61f);
    }

    private SpannableString generateCenterSpannableText() {

        SpannableString s = new SpannableString("MPAndroidChart\ndeveloped by Philipp Jahoda");
        s.setSpan(new RelativeSizeSpan(1.7f), 0, 14, 0);
        s.setSpan(new StyleSpan(Typeface.NORMAL), 14, s.length() - 15, 0);
        s.setSpan(new ForegroundColorSpan(Color.GRAY), 14, s.length() - 15, 0);
        s.setSpan(new RelativeSizeSpan(.8f), 14, s.length() - 15, 0);
        s.setSpan(new StyleSpan(Typeface.ITALIC), s.length() - 14, s.length(), 0);
        s.setSpan(new ForegroundColorSpan(ColorTemplate.getHoloBlue()), s.length() - 14, s.length(), 0);
        return s;
    }

